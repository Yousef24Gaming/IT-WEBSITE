document.getElementById("up-btn").addEventListener("click", function () {
  window.scrollTo({
    top: 0,
    behavior: "smooth",
  });
});
document.getElementById("text-course").addEventListener("click", function () {
  const textcourse = document.getElementById("t-course");
  const videocourse = document.getElementById("v-course");
  const documentcourse = document.getElementById("d-course");

  textcourse.style.display = "block";
  videocourse.style.display = "none";
  documentcourse.style.display = "none";
});
