document.getElementById("bto1").addEventListener("click", function () {
  const pdfContainer = document.getElementById("pdf-1");
  const pdfbtn = document.getElementById("bto1");
  if (pdfbtn.innerHTML == "Open") {
    pdfContainer.style.display = "block";
    pdfbtn.innerHTML = "Close";
  } else {
    pdfContainer.style.display = "none";
    pdfbtn.innerHTML = "Open";
  }
});
